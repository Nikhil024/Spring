package com.growagro.Bean;


public class ImagesBean {

	private int id;
	private String userpicture;
	private String userpicturelocation;
	private String fruitsvegetablesname;
	private String fruitvegetablespiclocation;
	private String uploaddate;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUserpicture() {
		return userpicture;
	}
	public void setUserpicture(String userpicture) {
		this.userpicture = userpicture;
	}
	public String getUserpicturelocation() {
		return userpicturelocation;
	}
	public void setUserpicturelocation(String userpicturelocation) {
		this.userpicturelocation = userpicturelocation;
	}
	public String getFruitsvegetablesname() {
		return fruitsvegetablesname;
	}
	public void setFruitsvegetablesname(String fruitsvegetablesname) {
		this.fruitsvegetablesname = fruitsvegetablesname;
	}
	public String getFruitvegetablespiclocation() {
		return fruitvegetablespiclocation;
	}
	public void setFruitvegetablespiclocation(String fruitvegetablespiclocation) {
		this.fruitvegetablespiclocation = fruitvegetablespiclocation;
	}
	public String getUploaddate() {
		return uploaddate;
	}
	public void setUploaddate(String uploaddate) {
		this.uploaddate = uploaddate;
	}

}
